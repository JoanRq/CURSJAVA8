public class Division {
  public static void main(String[] args) {
    System.out.println(9 / 3);  // 1.
    System.out.println(9 % 3);  // 2.
    System.out.println(10 / 3); // 3.
    System.out.println(10 % 3); // 4.
    System.out.println(11 / 3); // 5.
    System.out.println(11 % 3); // 6.
    System.out.println(-12 / 3); // 7.
    System.out.println(-12 % 3); // 8.
    System.out.println(-13 / 3); // 9.
    System.out.println(-13 % 3); // 10.    
    System.out.println(14 / -3); // 11.
    System.out.println(14 % -3); // 12.    
  }
}