public class UnBucle {
  public static void main(String[] args) {
    
    int i = 0;
    while (i<10) { 
      System.out.println("Hola");
      i++;
    }
    
    for(int j=0; j<10; j++) {
      System.out.println("Adios");
    }
    
    int k=0;
    do {
      System.out.println("Hola");
      k++;
    } while (k<10);
  }
}

















